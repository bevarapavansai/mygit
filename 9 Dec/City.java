package com.technoelevate.scannerclass;
	public class City {
		
		private String city;
		private String famous;
		
		public City(String city, String famous) {
			super();
			this.city=city;
			this.famous=famous;
		}

		public String getCity() {
			return city;
		}
		public void setCity(String city) {
			this.city = city;
		}

		public String getFamous() {
			return famous;
		}

		public void setFamous(String famous) {
			this.famous = famous;
		}

	}



